package UD04;

public class ex4 {
	public static void main (String[] args) {
		int N=10;
		int resultado;
		
		System.out.println("Valor inicial de N = "+N);
		resultado=N+77;
		System.out.println(N+ " + 77 = "+resultado);
		N=resultado;
		resultado=N-3;
		System.out.println(N+ " - 3 = "+resultado);
		N=resultado;
		resultado=N*2;
		System.out.println("El doble de "+N+ " es "+resultado);
		
	}
	
}
